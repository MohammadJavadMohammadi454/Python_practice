# تعریف کلاس‌های مختلف برای رستوران
class غذا:
    def __init__(self, نام, دسته‌بندی, قیمت, زمان_پخت):
        self.نام = نام
        self.دسته‌بندی = دسته‌بندی
        self.قیمت = قیمت
        self.زمان_پخت = زمان_پخت

class پرسنل:
    def __init__(self, نام, نقش, نرخ_ساعتی):
        self.نام = نام
        self.نقش = نقش
        self.نرخ_ساعتی = نرخ_ساعتی

class سفارش:
    def __init__(self, غذا, تعداد, نام_مشتری):
        self.غذا = غذا
        self.تعداد = تعداد
        self.نام_مشتری = نام_مشتری
        self.قیمت_کل = self.غذا.قیمت * self.تعداد  # دسترسی به قیمت غذا
        self.زمان_پخت = self.غذا.زمان_پخت * self.تعداد  # دسترسی به زمان پخت غذا

    def جزئیات_سفارش(self):
        return f"سفارش برای {self.نام_مشتری}: {self.تعداد}x {self.غذا.نام} - قیمت کل: {self.قیمت_کل} تومان"

class رستوران:
    def __init__(self):
        self.غذاها = []
        self.پرسنل = []
        self.سفارشات = []

    def اضافه_کردن_غذا(self, غذا):
        self.غذاها.append(غذا)

    def اضافه_کردن_پرسنل(self, پرسنل):
        self.پرسنل.append(پرسنل)

    def ثبت_سفارش(self, سفارش):
        self.سفارشات.append(sفارش)

    def محاسبه_درآمد_کل(self):
        return sum([سفارش.قیمت_کل for سفارش in self.سفارشات])

    def تخصیص_پرسنل_به_سفارش(self, سفارش, پرسنل):
        # محاسبه زمانی که برای پخت غذا نیاز است
        زمان_لازم = سفارش.زمان_پخت
        # محاسبه هزینه نیروی انسانی برای این زمان
        هزینه = پرسنل.نرخ_ساعتی * (زمان_لازم / 60)  # تبدیل دقیقه به ساعت
        return هزینه

# غذاها
غذا_ایرانی1 = غذا("کباب کوبیده", "ایرانی", 12000, 30)  # قیمت 12000 تومان، زمان پخت 30 دقیقه
غذا_ایرانی2 = غذا("قورمه سبزی", "ایرانی", 15000, 90)  # قیمت 15000 تومان، زمان پخت 90 دقیقه
غذا_فرنگی1 = غذا("پاستا", "فرنگی", 20000, 25)  # قیمت 20000 تومان، زمان پخت 25 دقیقه
غذا_فرنگی2 = غذا("پیتزا", "فرنگی", 25000, 40)  # قیمت 25000 تومان، زمان پخت 40 دقیقه

# پرسنل
پرسنل1 = پرسنل("علی", "پخت", 10000)  # هزینه به ازای هر ساعت 10000 تومان
پرسنل2 = پرسنل("زهرا", "بسته بندی", 8000)  # هزینه به ازای هر ساعت 8000 تومان

# ساخت رستوران و افزودن غذاها و پرسنل
رستوران = رستوران()
رستوران.اضافه_کردن_غذا(غذا_ایرانی1)
رستوران.اضافه_کردن_غذا(غذا_ایرانی2)
رستوران.اضافه_کردن_غذا(غذا_فرنگی1)
رستوران.اضافه_کردن_غذا(غذا_فرنگی2)

رستوران.اضافه_کردن_پرسنل(پرسنل1)
رستوران.اضافه_کردن_پرسنل(پرسنل2)

# ثبت سفارش‌ها
سفارش1 = سفارش(غذا_ایرانی1, 3, "مشتری1")  # 3 عدد کباب کوبیده
سفارش2 = سفارش(غذا_فرنگی1, 2, "مشتری2")  # 2 عدد پاستا

رستوران.ثبت_سفارش(سفارش1)
رستوران.ثبت_سفارش(سفارش2)

# نمایش جزئیات سفارش‌ها
for سفارش in رستوران.سفارشات:
    print(سفارش.جزئیات_سفارش())

# محاسبه درآمد کل رستوران
print(f"درآمد کل: {رستوران.محاسبه_درآمد_کل()} تومان")

# تخصیص پرسنل به سفارش‌ها و محاسبه هزینه نیروی انسانی
for سفارش in رستوران.سفارشات:
    هزینه_پرسنل = رستوران.تخصیص_پرسنل_به_سفارش(سفارش, پرسنل1)
    print(f"هزینه پرسنل برای {سفارش.غذا.نام}: {هزینه_پرسنل:.2f} تومان")